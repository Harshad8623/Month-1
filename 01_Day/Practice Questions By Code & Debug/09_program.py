marks = int(input("Enter Your Marks : "))
if marks >=90 :
    print("Grade : A")
elif marks >=80 :
    print("Grade : B")
elif marks >=70 :
    print("Grade : C")
elif marks >=60 :
    print("Grade : D")
elif marks >=50 :
    print("Grade : E")
elif marks >=40 :
    print("Grade : F")
else :
    print("Grade : Fail")  